<h1>Strategia e tattica</h1>

<p>Spesso nel linguaggio quotidiano utilizziamo le parole <b>strategia</b> e <b>tattica</b> come sinonimi.
Ma in verità questi due termini hanno significati diversi. Lo sanno molto bene gli scacchisti e chi si occupa di
pianificazione aziendale.</p>

<div class="card border-primary mb-3">
    <div class="card-header">Definizione</div>
    <div class="card-body">
        <p class="card-text">La <b>strategia</b> è un piano d'azione a lungo termine.</p>
        <p class="card-text">La <b>tattica</b> è un insieme di piccoli passi che portano a raggiungere un
            obiettivo ridotto.</p>
    </div>
</div>

<p>Per esempio, la mobilità è una <b>strategia</b>: è un piano d'azione che va mantenuto lungo la maggior parte
della partita e non porta a obiettivi immediati.</p>

<p>In questo capitolo, invece, cominceremo a vedere come comportarsi sui bordi; in particolare analizzeremo gli
attacchi fondamentali, che sono tutti esempi di <b>tattica</b>. Ogni attacco è infatti una manovra finalizzata
alla conquista di un angolo o di un insieme di pedine stabili.</p>

<p>Nei capitoli successivi alterneremmo nuove strategia e nuove tattiche.</p>

<p>Tanto per fare un paragone, negli scacchi è rimasta storica l'affermazione di Tichard Teichmann:</p>

<cite>Gli scacchi sono al 99% tattica.</cite>

<p>Probabilmente questa valutazione è esagerata, ma è innegabile che negli scacchi l'analisi tattica è dominante su
quella scacchistica.</p>

<p>Nell'Othello, secondo me, c'è un bilanciamento maggiore tra strategia e tattica. All'inizio della partita
è dominante la strategia: è importante scegliere come implementare la mobilità ed è importante scegliere opportunamente
l'apertura. Poi, man mano che la partita evolve, in particolare quando iniziano le manovre sui bordi, la tattica
diventa sempre più importante, fino a diventare dominante alla fine dalla partita. Anzi: quando nel finale non rimane
altro che contare le pedine, abbiamo tattica al 100%.</p>