<h1>Una visione più ampia per gli attacchi ai cinque</h1>

<p>L'analisi di una posizione e la scelta della mossa successiva non può limitarsi all'applicazione di un'unica
strategia o un'unica tattica. Questo significa che se il tuo avversario ha un cinque attaccabile, non è detto
che attaccarlo, e attaccarlo subito, sia la scelta migliore. Devi vagliare la tua possibilità anche alla luce
    di tutte le altre strategie.</p>

<p>Limitandoci, per ora, agli elementi che già conosci, ci sono alcune domande a cui dovresti rispondere prima di
    sferrare l'attacco a un cinque.</p>

<ul>
    <li>Quanto è vantaggioso per il tuo avversario, l'attaccato, prendere l'angolo che stai cedendo?
    Più in dettaglio:
        <ul>
            <li>Chi ha la parità nella regione di attacco (caselle indicate con la lettera T nel diagramma 1)?</li>
            <li>Qual è l'effetto della presa dell'angolo sul bordo adiacente (lettera U)?</li>
            <li>Quanti tempi di gioco guadagna l'avversario?</li>
        </ul>
    </li>
    <li>Quanto è vantaggioso per te, l'attaccante, prendere il cinque e l'angolo?
        Più in dettaglio:
        <ul>
            <li>Chi ha la parità nella regione opposta all'attacco (lettera V)?</li>
            <li>Qual è l'effetto della presa dell'angolo sul bordo adiacente (lettera Z)?</li>
            <li>Quanti tempi di gioco guadagni?</li>
        </ul>
    </li>
</ul>

