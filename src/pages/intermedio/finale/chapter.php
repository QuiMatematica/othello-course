<h1>Il finale</h1>

<p>Mancano una quindicina di mosse alla fine e oramai la tavola da gioco offre pochi
spazi liberi. E' il momento in cui bisogna concretizzare i vantaggi
faticosamente ottenuti durante l'apertura e il centro-partita, ma è anche quello
in cui ogni errore avrà la caratteristica dell'inderogabilità. Spesso un finale giocato
male vanifica tutto ciò che di buono si è fatto fino a quel momento e conduce a una
misera sconfitta. Vediamo dunque come è meglio comportarsi in questo cruciale
momento della partita.</p>

<p>Innanzitutto è buona cosa capire che è teoricamente possibile determinare
il <a href="ultime-due-mosse.php">finale perfetto</a>. Il procedimento non è difficile nelle ultime cinque-sei mosse;
    ma diventa più complicato all'aumentare del numero di caselle libere. Purtroppo in questo i calcolatori ci battono
    (sono più veloci e hanno molta più memoria di noi), ma è comunque importante conoscere come contare il finale perfetto
    e provarci almeno nelle
ultime mosse.</p>

<p>Ma come fare quando abbiamo una decina di caselle libere, ci rendiamo conto che il vantaggio è minimo, e -
    ovviamente - vogliamo vincere la partita?
    Abbiamo bisogno di strumenti che ci permettano di selezionare le sequenze più
    vantaggiose: ci servono delle euristiche.</p>

<div class="card border-primary mb-3">
	<div class="card-header">Definizione</div>
	<div class="card-body">
        <p class="card-text">Le <b>euristiche</b> (dal greco <i>heurískein</i>: trovare, scoprire) sono procedimenti mentali
            intuitivi e sbrigativi, scorciatoie mentali, che permettono di costruire un’idea generica su un argomento
            senza effettuare troppi sforzi cognitivi. Sono strategie veloci utilizzate di frequente per giungere
            rapidamente a delle conclusioni.</p>
	</div>
</div>

<p>In questo capitolo ti presenterò le più semplici e importanti euristiche per il finale:</p>
<ul>
    <li>la più importante di tutte è la parità, strategia che abbiamo già incontrato e che riprenderemo in una pagina apposita;</li>
    <li>vedremo quindi come non sia necessario contare tutte le possibili sequenze, ma possiamo selezionare quelle più
    probabili e più significative per il finale;</li>
    <li>vedremo quando sequenze in aree diverse sono tra loro dipendenti o indipendenti e come sfruttare questo legame;</li>
    <li>infine faremo qualche riflessione sulle mosse sicure e sulla loro importanza nei finali.</li>
</ul>

<p>Altre euristiche verranno presentate nella sezione avanzata del corso.</p>

<p>Mettere insieme il tutto è quello che ti permetterà di diventare un ottimo giocatore di Othello.
E può essere buona cosa allenarsi
costantemente sui finali, utilizzando programmi che propongono posizioni da risolvere in modo
    perfetto (o per lo meno vincente) in un tempo limitato.</p>

<p>Questo capitolo si conclude, infine, con qualche riflessione matematica sulla complessità del gioco.</p>
