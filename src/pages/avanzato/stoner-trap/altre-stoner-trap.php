<h1>Altri esempi di Stoner trap</h1>

<p>Il diagramma <span data-board-ref="altre-1"></span> riporta la situazione alla 38-esima mossa della semifinale del Campionato
Italiano 1984, tra Paolo Ghirardato (bianco) e Piero Zama (nero) (esempio tratto dal libro
di Brusca, Maccheroni, Puzzo). Il bianco è in vantaggio perché ha chiuso il nero in
maniera pressoché completa. Ma prima di gettare la spugna Piero tenterà di rendere la
vita difficile al suo avversario eseguendo una variante delle Stoner trap.</p>

<board data-type="show" data-label="altre-1" data-file="altre-stoner-trap-1.json"
       data-caption="Variante di Stoner trap."></board>

<p>Secondo la classifica di questo corso sarebbe una Stoner trap di tipo A, visto che il bianco dovrebbe giocare
in <b>C1</b> per non cedere l'angolo. Ma la presenza della pedina <b>B1</b> la rende più simile, per quanto riguarda
la parità, alle Stoner trap di tipo C.</p>

<p>Il diagramma <span data-board-ref="altre-2"></span> riporta la posizione alla 36-esima mosse della finale del Campionato
del Mondo 1983 tra il giapponese Ken Ichi Ishii (bianco) e l'inglese Imre Leader (nero)
(anche questo esempio è tratto dal libro di Brusca, Maccheroni, Puzzo). La struttura a
nord è un quattro sbilanciato <i>bucato</i>. Se il nero volesse chiudere il quattro avversario
con <b>C1</b>, seguita dalla forzata <b>D1</b>, non sarebbe possibile giocare una Stoner trap. Ma è
possibile attaccare la formazione anche se è bucata.</p>

