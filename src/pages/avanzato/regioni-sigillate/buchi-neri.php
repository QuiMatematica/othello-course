<p>Un caso particolare di <i>regione sigillata</i> è rappresentato dai buchi neri.</p>

<div class="card border-primary mb-3">
	<div class="card-header">Definizione</div>
	<div class="card-body">
		<p class="card-text">Chiamiamo <b>buco nero</b> (in inglese <i>black hole</i>) una regione formata da una sola casella in cui
            un solo colore può muovere.</p>
	</div>
</div>

<p>Un buco nero è certamente uno strumento molto potente per il colore che ci può muovere.</p>
<ul>
    <li>Può essere utilizzato come ultima mossa della partita.</li>
    <li>Può essere utilizzato come <i>mossa d'attesa</i> durante una manovra di <i>feeding the opponent</i>.</li>
    <li>Può essere utilizzato come <i>mossa jolly</i> per costringere l'avversario a muovere in una posizione suicida.</li>
    <li>E chissà quanti altri mille usi...</li>
</ul>

<p>Particolarmente interessante è quando un buco nero si trova il corrispondenza di una casella X.
    In questa pagina mi concentrerò su queste situazioni.</p>

<h2>Primo esempio</h2>

<p>Nella posizione del diagramma <span data-board-ref="suekuni14"></span> vogliamo
    concentrarci sul buco nero rappresentato dalla casella X <b>G7</b>.</p>

<board data-type="show" data-label="suekuni14" data-file="suekuni14.json"></board>

<p>Oltre a <b>G7</b>, anche la casella d'angolo <b>H8</b> potrebbe essere considerata un buco nero.
    Al momento il nero non ha accesso ad <b>H8</b>, ma visto che
le pedine <b>G8</b> e <b>H7</b> sono bianche, il bianco vi può giocare solo se il nero gioca prima
in <b>G7</b>.</p>

<p>Ma tocca al nero, che ovviamente vuole sfruttare al massimo tutte le opportunità.</p>

<p>Quindi diventa piuttosto facile trovare la strategia vincente.</p>
<ul>
    <li>Prima di tutto dovrà trovare il modo di giocare in <b>H8</b>.</li>
    <li>Dopodiché potrà sfruttare anche il buco nero in <b>G7</b>.</li>
</ul>

<p>I vantaggi derivanti da questa manovra sono tali che è accettabile cedere un angolo
e un intero bordo all'avversario. Il resto della tavola diventerà tutto suo.</p>

