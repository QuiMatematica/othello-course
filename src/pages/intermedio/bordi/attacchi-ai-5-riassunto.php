    <h1>Gli attacchi ai cinque: riassunto</h1>

    <p>Ti ricordo che stiamo parlando di cinque formati da pedine bianche, disposti sul lato sud
    tra le caselle B8 ed F8; inoltre le caselle F7 e G6 sono occupate da una pedina.</p>

	<p>Abbiamo visto che si presentano le seguenti situazioni:</p>

	<ul>
	  <li>se la riga 7 è occupata da sole pedine bianche, l'attacco funziona sempre, indipendentemente
	      dalla formazione di pedine sulla colonna G;</li>
	  <li>se la riga 7 è occupata da almeno una pedina nera e la colonna G da sole pedine bianche,
	      l'attacco non funziona;</li>
	  <li>se la riga 7 è occupata da almeno una pedina nera e la colonna G da sole pedine nere,
	      l'attacco non funziona;</li>
	  <li>in tutti gli altri casi l'attacco generalmente funziona.</li>
	</ul>

	<p>Attenzione: nell'ultimo punto ho detto "generalmente funziona" perché in particolari condizioni,
	ad attacco già in atto, è possibile che il bianco riesca a portarsi in una situazione simile
	alla seconda o alla terza in modo da vanificare l'attacco.</p>

	<p>Come conseguenza di ciò, se stai giocando su un bordo ricorda:</p>

	<ul>
	  <li>evita di girare anche tutte le pedine della riga 7, altrimenti il tuo cinque sarebbe sempre
	  attaccabile;</li>
	  <li>fai in modo che le pedine della colonna G siano dello stesso colore;</li>
	  <li>se vieni attaccato e l'attacco sta funzionando, se puoi, non prendere subito l'angolo ma
	  cerca di ricondurti a una situazione in cui le pedine della colonna G siano dello stesso
	  colore, oppure cerca di ottenere degli altri vantaggi in altre zone della scacchiera.</li>
	</ul>
