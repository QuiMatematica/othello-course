<h2>Il <i>pattern</i></h2>

<p>Il <i>pattern</i> (diagramma <span data-board-ref="quattro-caselle-2-3"></span>) che permette questo <i>tesuji</i>
    prevede la presenza di:</p>
<ul>
    <li>una regione di quattro caselle (riquadro rosso);</li>
    <li>la possibilità di muovere nella casella <b>C</b> (freccia gialla);</li>
    <li>la possibilità di muovere nell'angolo (frecce viola).</li>
</ul>

