<h1>I bordi</h1>

<p>Fin dalla prime partite ci si rende conto che i bordi sono posizioni strategicamente importanti:</p>
<ul>
    <li>le pedine sui bordi godono di una relativa stabilità e sono più facili da controllare
    delle pedine centrali;</li>
    <li>per contro le formazioni di bordo possono essere attaccate: tipicamente l'attaccante cede
        un angolo per guadagnare un alto numero di pedine stabili.</li>
</ul>

<p>Più in generale, il gioco sui bordi deve essere guidato da tre obiettivi:</p>

<ul>
  <li>conquistare dei
      <a href="../tempi-di-gioco/chapter.php" title="I tempi di gioco"><b>tempi di gioco</b></a>
      e non consentire all'avversario di farlo;</li>
  <li>preparare gli spazi adeguati per giocare la
      <a href="../parita/chapter.php" title="La parità"><b>parità</b></a>;</li>
  <li>ottenere delle <b>formazioni non attaccabili</b> e costringere l'avversario a prendere delle
  <b>formazioni attaccabili</b>.</li>
</ul>

<p>I primi due obiettivi sono già stati trattati nei capitoli precedenti.</p>

<p>In questo capitolo cominceremo ad approcciare il terzo obiettivo con una serie di definizioni
    e di osservazioni tattiche di natura generale.
Nei due capitoli successivi verranno mostrate le forme più semplici di attacchi e trappole.
La trattazione di tutti gli attacchi, per lo meno quelli formalizzati come <i>pattern</i>,
viene rimandata alla sezione avanzata del corso.</p>