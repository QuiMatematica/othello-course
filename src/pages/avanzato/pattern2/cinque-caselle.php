<p>Giocare in una regione di cinque caselle è sempre vantaggioso, perché grazie alla parità chi fa la prima mossa
dovrebbe riuscire a fare anche l'ultima mossa. Ma perché non cercare di sfruttare una regione di cinque caselle
per giocare quattro delle mosse a disposizione, guadagnando così tre tempi di gioco?</p>

<p>Il diagramma <span data-board-ref="cinque-caselle-1"></span> mostra proprio una situazione di questo tipo.
Il nero ha la possibilità di giocare nella regione di cinque caselle a nord-ovest. Ma grazie al controllo della
diagonale potrà giocare quattro mosse delle cinque a disposizione.</p>

