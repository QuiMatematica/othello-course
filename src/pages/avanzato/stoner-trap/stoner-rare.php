<h2>Fare in modo che non ci siano le condizioni</h2>

<p>Le condizioni per realizzare una Stoner trap non sono facili da ottenere, in particolare il controllo della diagonale.
    Quindi al giocatore minacciato basta assicurarsi di avere sempre una pedina sulla diagonale e l'attacco è sventato.</p>

<p>Il diagramma <span data-board-ref="juigner-berg-2"></span> mostra la posizione dopo la mossa 26 della
    seconda finale del Campionato Europeo 2025. Rispetto alla precedente i colori sono invertiti:
    Arthur Juigner gioca con il nero e Matthias Berg gioca con il bianco.</p>

<board data-type="show" data-label="juigner-berg-2" data-file="juigner-berg-2-2025-26.json"
       data-caption="Juigner - Berg<br>Campionato Europeo 2025"></board>

<p>La posizione è piuttosto bilanciata. Entrambi i colori non devono assolutamente perdere di vista la mobilità.
    Tocca al nero, al quale non conviene né giocare a nord-est, né a sud-ovest. L'unica mossa che sembrerebbe
    interessante dal punto di vista della mobilità è <b>H7</b>. Infatti con tale mossa il bianco non ottiene
    nessuna nuova mossa buona. Inoltre il nero gira la pedina <b>F5</b> che gli permetterebbe, in seguito,
    di giocare in <b>B5</b>. Ma...</p>

<p>Ma muovendo in <b>H7</b> il nero forma un bordo sbilanciato, ed è uno di quelli attaccabili con una Stoner trap.
    Ma è effettivamente attaccabile? Subito no, perché la diagonale è tagliata dalla pedina <b>E4</b> che diventa nera.
    Quindi si può osare! Nel diagramma <span data-board-ref="juigner-berg-2-seguito"></span> vedi il seguito della
partita.</p>

<board data-type="show" data-label="juigner-berg-2-seguito" data-file="juigner-berg-2-2025-26-seguito.json"
       data-caption="Juigner - Berg<br>Campionato Europeo 2025"></board>

<p>Anche in questo caso la Stoner trap non si è vista, anche se c'era la formazione sbilanciata adatta. Tuttavia
non c'erano le condizioni per eseguire l'attacco e il nero ha trovato una buona scappatoia per evitare
l'attacco.</p>

<h2>Se devi subire... fallo consapevolmente</h2>

<p>Ormai l'hai capito: nell'Othello non ci sono assoluti. Ogni strategia sembra fatta apposta per avere le sue
eccezioni. Anche le Stoner trap hanno questa caratteristica: possono esserci delle situazioni in cui può essere
vantaggioso subirle.</p>

<p>Facciamo un bilancio <i>dare-avere</i> di una Stoner trap andata buon fine.</p>

