<p>Certamente il vantaggio che si ottiene dal controllo di una <i>mini-linea</i> è temporaneo: presto
    arriveranno altre pedine sulla linea stessa e più difficile sarà mantenere il controllo.
Tuttavia più a lungo si riuscirà a mantenere questo controllo maggiore sarà il vantaggio derivante.</p>

