        <h1>I bordi</h1>

        <p>Fin dalla prime partite ci si rende conto che i bordi della tavola rivestono un
            ruolo privilegiato.</p>

        <p>Da una parte le pedine sui bordi godono di una relativa stabilità e sono più facili da controllare
            delle pedine centrali. Dall'altra le formazioni di bordo possono essere attaccate, cedendo un angolo
            ma guadagnando un alto numero di pedine stabili.</p>

        <p>Il gioco sui bordi deve essere guidato principalmente da tre obiettivi:</p>

        <ul>
          <li>conquistare dei <b>tempi di gioco</b> e non consentire all'avversario di farlo;</li>
          <li>conquistare delle <b>posizioni non attaccabili</b> o costringere l'avversario a prendere
          <b>posizioni attaccabili</b>;</li>
          <li>preparare degli spazi adeguati per giocare la <b>parità</b>.</li>
        </ul>

        <p>Del primo obietto si è già parlato nel
            <a href="../tempi-di-gioco/chapter.php" title="I tempi di gioco">capitolo precedente</a>.</p>

        <p>Il terzo obiettivo riguarda la parità che verrà spiegata nel prossimo capitolo.</p>

        <p>In questo capitolo verrà trattato in modo approfondito il secondo obiettivo:
        dopo una prima presentazione delle diverse formazioni di bordo verranno mostrate
        le forme più semplici di attacchi e trappole.</p>