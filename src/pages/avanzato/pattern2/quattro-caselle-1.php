<p>Una regione d'angolo formata da quattro caselle può essere oggetto di più di uno <i>swindle</i>.</p>

<p>Il caso mostrato in questa pagina è reso possibile dalla presenza di due bordi pieni come, per esempio,
due quattro pieni.</p>

