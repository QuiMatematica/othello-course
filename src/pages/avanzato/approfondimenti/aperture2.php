    poco interessante un gioco in cui troppo spesso i top players giocano solo a memoria. La voglia di mantenere il
    gioco variegato ha portato al debutto dell'Europeo XOT, a Settembre 2026, e si parla anche di modifiche al sistema
    dei punteggi per scoraggiare chi decide di portare a casa un "pareggio facile," seguendo una draw sequence tra
    quelle che si conoscono per intero.</p>

<p>Insomma, stiamo entrando in una nuova era per l'Othello, e potreste presto aver bisogno di nuovi strumenti. Nuove
    aperture che in pochi conoscono, alternative che rendano il gioco più creativo e meno prevedibile. È quello che
    dicevamo l'altro giorno sul <a href="aperture.php">liberarsi dalla prigione delle aperture</a>:
    il coraggio rende liberi, ti fa godere di più il gioco, e se preparate la strada per bene, non
    cambia in maniera visibile le vostre probabilità di vittoria. Io lo chiamo <i>Happy Othello</i>, per quel particolare
    fenomeno che ti porta a prenderla a ridere anche se alla fine perdi, perché hai dato vita a una partita più
    emozionante del solito: l'hai portata su un puro piano strategico, esente da abilità mnemoniche, in cui vince chi
    <i>vede</i> più cose dell'avversario.</p>

<p>Se state pensando di cambiare strategia e gettarvi nella mischia delle aperture che pochi oserebbero, o se siete già
    in questa lunghezza d'onda e vorreste scoprirne qualcun'altra che magari vi manca, siete nel posto giusto: da un
    anno a questa parte, io non faccio altro che giocare aperture folli, valutandone la filosofia, le opportunità e i
    rischi collegati. Ci saranno sicuramente giocatori più esperti di me sull'argomento, ma finché non sono loro a
    scriverci su, la lista qui sotto è un buon punto di partenza per iniziare a capire quali aperture sentite più
    compatibili col vostro stile.</p>

<p>Qui sotto trovate una lista di aperture poco note, cinque per colore, con una breve descrizione delle loro
    potenzialità e un'indicazione della loro rarità e valutazione posizionale. Ovviamente tutte hanno valutazione
    negativa, ma quello non deve scoraggiarvi: quelle a valutazione 0 o positiva sono proprio quelle che conoscono
    tutti e su cui ha grosso peso la componente mnemonica, ma a parità di conoscenza della loro filosofia sottostante,
    quelle qui sotto non vi offrono svantaggi palpabili. Partire a -4 o -8 a mossa 4, con tutta la partita davanti,
    non vuol dire nulla, e il vantaggio è che l'avversario uscirà dal <i>book</i> e dal gioco di memoria molto prima,
    quindi le possibilità di vittoria sono paragonabili.</p>

