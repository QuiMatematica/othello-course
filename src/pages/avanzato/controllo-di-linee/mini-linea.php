<h1>Controllo di mini-linee</h1>

<p>Nelle pagine precedenti abbiamo visto l'importanza di controllare le linee più critiche soprattutto nella fase
finale della partita. Tali linee critiche sono le diagonali principali, i controbordi e le diagonali C.</p>

<p>Tuttavia il controllo di linee può essere utilizzato fin dall'apertura.</p>

<p>In apertura, chiaramente, ci sono poche pedine sulla othelliera. Le linee quindi sono corte, formate da due, tre
o quattro pedine. Per questo preferisco chiamarle <i>mini-linee</i>, proprio per sottolineare il loro carattere
di brevità.</p>

<p>Tra queste sono più significative le <i>mini-diagonali</i> che sembrano avere una rilevanza strategica
maggiore rispetto alle <i>mini-righe</i> e <i>mini-colonne</i>.</p>

<p>Il controllo delle mini-linee in apertura ha gli stessi effetti del controllo delle linee critiche nel finale.</p>

