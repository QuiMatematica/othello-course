<h2>Condizioni per la riuscita dell'attacco</h2>

<p>Deve esserci una formazione di bordo sbilanciata da attaccare. Nell'esempio è un tre (diagramma 2), ma vedremo che è
possibile attaccare altre formazioni.</p>

