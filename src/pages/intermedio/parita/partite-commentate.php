<p>La mossa è al nero e Caspard si gioca l'ultima chance per poter ancora sperare
di accedere alla semifinale. Analizziamo gli aspetti statici della posizione senza
entrare, per il momento, nel dettaglio delle sequenze giocabili:</p>

<ul>
  <li>Il nero ha una debolezza rappresentata dalla pedina in A2: un bordo sbilanciato.
  La debolezza consiste nel fatto che una mossa del bianco in A3 "costringerebbe" il nero
  a prendere immediatamente in A7, pena la perdita senza compensazione di due tempi di gioco
  (A3 e A1).</li>
  <li>Il bianco non ha la possibilità di giocare per primo nello spazio dispari formato da
  tre caselle vuote e adiacente all'angolo sud-ovest. In altre parole, ha perso la parità
  locale in quell'angolo e globale in tutta la scacchiera. Infatti, se nessuno dei due
  giocatori sarà costretto a passare, il nero giocherà l'ultima mossa nel resto della
  scacchiera e due mosse su tre nell'angolo sud-ovest.</li>
  <li>Il bianco ha un cinque sul bordo sud. Il nero potrà sfruttare tale debolezza nelle
  prossime mosse guadagnando un tempo e attaccando il cinque con una mossa in G7. Anche il nero
  ha un cinque sul bordo est, ma questa debolezza è già stata sfruttata dal bianco che lo ha
  attaccato alla mossa 42.</li>
</ul>

<p>Non mi sembra che ci siano altri elementi strategici da prendere in considerazione. Per
esempio, non esistono rischi di controllo delle diagonali da parte di nessuno dei giocatori
perché la pedina nera in D5 e quella bianca in E5 non potranno più cambiare colore.</p>

<p>Qual è la mossa giusta? Il diagramma 3 riporta la sequenza giocata da Caspard. Il
diagramma 4 riporta la sequenza derivante dalla scelta corretta.</p>

