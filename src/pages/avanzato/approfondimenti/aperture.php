<p>Tutto molto bello. Poi però ti rendi conto che è una prigione. E uscire dalla prigione delle aperture è parecchio
    difficile.</p>

<p>Ti rendi conto di dover uscire quando capisci che questa logica ha dei limiti. Alex Koh me lo disse anni fa, con la
    tipica diplomazia asiatica: "Se continui su questa strada, forse un giorno sarai tra i migliori al mondo. Ma è una
    strada lunga. E potrebbe essercene una più veloce..." Allora era troppo presto, non comprendevo appieno l'urgenza
    di abbandonare l'unica cosa che mi dava sicurezza nel mio stile di gioco. Ma la gente continuava a dirmelo. Qualche
    tempo dopo, Matthias Berg me la lanciò come una sfida e, siccome mi piacciono le sfide, funzionò. Poi un giorno
    sono andato a Wrocław a giocare contro i forti giocatori polacchi, e Milosz Cupial mi spiegò perché avevo perso:
    "Mi ero reso conto che conoscevi molto bene le aperture, quindi ho dovuto fare una mossa che ti spiazzasse,
    abbastanza presto da avere il tempo di recuperare lo svantaggio, sperando che tu facessi un errore. Sai, gioco a
    Othello da vent'anni..."</p>

