<h1>L'influenza</h1>

<p><b>L'influenza</b> è uno dei concetti chiave dell'approccio moderno alla
<a href="../../intermedio/mobilita/chapter.php">mobilità</a>. Essa conferma le
basi su cui finora abbiamo ragionato e offre qualche
elemento in più per valutare le posizioni di gioco.</p>

<p>Le pagine di questo capitolo sono tratte (con qualche modesto adattamento) dagli articoli
    del compianto Francesco Marconi dal titolo "Teoria dell'Othello",
    pubblicati su Othello News (il giornalino di collegamento degli anni 80 e 90)
    tra il 1994 e il 1995.</p>

<p>Sembra (ed è effettivamente) passato un secolo, eppure l'impostazione di Francesco è
tutt'ora molto attuale.</p>