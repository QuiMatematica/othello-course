<p>Così ho capito che, in fondo, è facile smantellare il vantaggio delle aperture. Un giocatore di esperienza potrebbe
    facilmente sapere l'apertura più a lungo di te, oppure conoscere più varianti di te, o ancora avere la confidenza
    giusta per uscire dal book presto e poi giocarsela in stile libero. E dall'altro lato, se tu hai scommesso così
    tanto sulla memorizzazione di aperture lunghe, finisci per avere più problemi di autostima nel gioco libero, quindi
    fuori dalle sequenze memorizzate. Era così che mi sentivo, e, che diamine, avevo bisogno di scoprire se avevo un
    vero talento per l'Othello o se ero arrivato fin qui solo grazie alla memoria e a un po' di riconoscimento di
    pattern.</p>

<p>E così, il mio complicato rapporto con le aperture mi ha portato alla gioia di giocare XOT. Sì, lo so, molti di voi
    ci erano già arrivati, ma abbiate pazienza, ho solo sei anni (anni-Othello, intendo). L'ho scoperto da poco.
    Recentemente c'è stata persino la possibilità di giocare partite XOT da 1 minuto su Othello Quest e, beh, non mi
    sono rimaste molte scuse per non provare. E con mia piacevole sorpresa, ho scoperto di non essere affatto male.
    Ho i miei strumenti. "Vedo cose", come in un film di Shyamalan. Metto in difficoltà giocatori esperti. Ho persino
    vinto il torneo XOT in Italia nel 2025, contro giocatori che mi danno sempre filo da torcere nelle partite
    regolari.</p>

<p>Tutti questi segnali mi dicevano che era ora di cambiare le cose. Ho iniziato a pensarci più seriamente, trattandola
    come un'analisi da completare. La vera domanda a quel punto era: di quanto scende la mia percentuale di vittorie se,
    invece di giocare "le aperture che so", tiro fuori subito qualcosa di folle a mossa 4 e giochiamo tutti allegramente
    fuori dal book per tutta la partita?</p>

<p>Non vi darò la risposta, ma a giudicare dal mio entusiasmo, potete probabilmente indovinarla. Quello che ho capito
    è che giocare un -8 alla sesta mossa non è poi così diverso dal seguire le aperture che conosci e arrivare alla
    mossa 45 a -4. Se sei un giocatore esperto, sopravvivere alle ultime quindici mosse potrebbe essere in realtà più
    facile che gestire qualcosa di insolito fin dall'inizio. E so che prima di metà partita un computer direbbe che
    sono in netto svantaggio. Ma chi se ne frega, tanto sto giocando contro un essere umano.</p>

<p>Quindi, il giorno esatto in cui ho vinto la mia dipendenza dalle aperture... beh, non so di preciso quando è stato,
    ma so che ora mi sento finalmente libero. E non ho problemi a svelarvi pubblicamente il mio nuovo stile di gioco,
    tanto ormai ho sbloccato la consapevolezza di poter inventare una nuova variante praticamente ogni settimana.</p>

<p>Anzi, se proprio vi ispira studiarvi due o tre aperture "folli" giocate pochissimo ma con varianti interessanti che
    possono sorprendere l'avversario... restate lì. Ne parliamo di nuovo tra qualche giorno.</p>