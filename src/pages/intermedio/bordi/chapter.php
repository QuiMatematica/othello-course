        <h1>I bordi</h1>

        <p>Fin dalla prime partite ci si rende conto che i bordi sono posizioni strategicamente importanti.</p>

        <p>Da una parte, le pedine sui bordi godono di una relativa stabilità e sono più facili da controllare
            delle pedine centrali. Dall'altra, le formazioni di bordo possono essere attaccate: si cede un angolo
            per guadagnare un alto numero di pedine stabili.</p>

        <p>Il gioco sui bordi deve essere guidato principalmente da tre obiettivi:</p>

        <ul>
          <li>conquistare dei <b>tempi di gioco</b> e non consentire all'avversario di farlo;</li>
          <li>ottenere delle <b>formazioni non attaccabili</b> e costringere l'avversario a prendere delle
          <b>formazioni attaccabili</b>;</li>
          <li>preparare gli spazi adeguati per giocare la <b>parità</b>.</li>
        </ul>

        <p>Del primo obiettivo, i tempi di gioco, si è già parlato nel
            <a href="../tempi-di-gioco/chapter.php" title="I tempi di gioco">capitolo precedente</a>.</p>

        <p>Il terzo obiettivo, la parità, verrà trattato nel prossimo capitolo.</p>

        <p>In questo capitolo cominceremo ad approcciare il secondo obiettivo:
        dopo una prima presentazione delle diverse formazioni di bordo verranno mostrate
        le forme più semplici di attacchi e trappole.</p>