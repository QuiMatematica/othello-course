<p>Valutare la convenienza dello scambio è un'operazione non semplice da fare in tempi brevi
ma è comunque opportuno tenere presente che nel finale di partita, le pedine tendono rapidamente a
diventare stabili e di conseguenza il valore degli angoli non è più predominante come nel centro
partita. In molti casi ci si può permettere di cederli all'avversario in cambio della parità.</p>
