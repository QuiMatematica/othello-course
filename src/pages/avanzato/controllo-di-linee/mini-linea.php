<p>Il vantaggio che si ottiene dal controllo di una <i>mini-linea</i> è chiaramente temporaneo perché presto
arriveranno altre pedine sulla linea stessa e più difficile sarà il controllo. Inoltre più pedine ci saranno
minore sarà il vantaggio derivante dal controllo... a meno ché non sia una delle linee critiche per il finale.</p>