<h1>Le Stoner trap in azione</h1>

<p>Mi ha colpito un'affermazione del maestro Paolo Bianchi scritta sulla chat nazionale della F.N.G.O.:</p>

<p class="text-center"><i>Belle le Stoner, ma anche molto rare a verificarsi.
        Nelle centinaia di partite giocate ne ho subito pochissime, forse solo una, da Caspard
        in un torneo milanese degli anni '90.</i></p>

<p>Ho cercato di ricordarmi quante Stoner trap ho giocato nelle mie partite.</p>

<p>Me ne ricordo molto bene una. 22 aprile 1990: avevo 14 anni. Ero al mio secondo torneo, in quel di Modena,
ospite dei simpaticissimi Angela Quattrocchi e Luca Ottani. Stavo giocando proprio contro Angela. E all'improvviso
eccola lì: la possibilità di giocare la mia prima Stoner trap. E ho vinto la partita.
    Purtroppo non ho più il referto, quindi non posso mostrarvela.</p>

<p>Altre... no! Certo non ricordo a memoria tutte le mie partite, ma ho un ricordo più o meno vago di alcune più significative.
E non ricordo nessun'altra Stoner trap.</p>

<p>Ma allora è vero che sono così rare? Al punto che forse non vale la pena studiarle?</p>

<h2>Stoner trap: the phantom menace</h2>

<p>Sì, la citazione non è casuale. <i>The phantom menace</i>: <i>la minaccia fantasma</i>, il titolo del primo episodio
della saga di Guerre Stellari.</p>

<p>Anche se non si vedono, le Stoner trap sono spesso dietro l'angolo. Sono una minaccia che può concretizzarsi
    da un momento all'altro. Ma <i>se le conosci le eviti</i>. E se le eviti, non si presentano in partita.</p>

<p>Ma state certi che se, ad alto livello, un giocatore ottiene una formazione sbilanciata, subito entrambi pensano
alla Stoner trap: l'uno per capire se può utilizzarla, l'altro per fare di tutto per evitarla.</p>

<p>Le condizioni per realizzare una Stoner trap non sono facili da ottenere, in particolare il controllo della diagonale.
Quindi al giocatore minacciato basta assicurarsi di avere sempre una pedina sulla diagonale e l'attacco è sventato.
Ma talvolta gli sforzi di mantenere una situazione di non attaccabilità possono costare cari, come la perdita di mobilità
in una regione.</p>

<p>Quindi tra due giocatori che conoscono bene il gioco, la Stoner trap può realizzarsi essenzialmente in due occasioni:</p>
<ul>
    <li>chi può essere attaccato non si è accorto che ci sono tutte le condizioni per subire l'attacco;</li>
    <li>chi può essere attaccato ha valutato meno dannoso subire l'attacco rispetto alle manovre necessarie a evitarlo.</li>
</ul>

<h2>Evitare di sbilanciarsi</h2>

<p>Primo giugno 2025, prima finale del Quarto Campionato Europeo, a Praga. La partita è tutta svizzera,
    tra Matthias Berg (nero) e Arthur Juigner (bianco). Il diagramma <span data-board-ref="berg-juigner-2025-37"></span>
    mostra la posizione dopo la mossa 37 del nero in <b>F8</b>.

<board data-type="show" data-label="berg-juigner-2025-37" data-file="berg-juigner-2025-37.json"
       data-caption="Berg - Juigner, Praga"></board>

<p>Il bianco deve necessariamente rispondere sul bordo sud, pena la perdita di due tempi di gioco. Ha due mosse tra cui
scegliere: incunearsi in <b>E8</b> o sbilanciarsi in <b>B8</b>.</p>

<p>Giocando in <b>E8</b> si forma una posizione che è nota come <i>Suekuni trap</i>, che studieremo più avanti nel corso.
E' una posizione svantaggiosa per il bianco, in particolare se si trova ad avere poche mosse: rischia di essere costretto
    a giocare in <b>B8</b> o in <b>G8</b>, lasciando l'intero bordo all'avversario. In questo momento la mobilità del bianco
è buona, anche se ha un'influenza piuttosto alta. Tuttavia non dobbiamo dimenticare che entrambe i colori avranno
poche mosse nel finale, quando ci saranno effettivamente poche caselle libere. Per riassumere, diciamo che è una posizione
    scomoda, non particolarmente pericolosa in questo momento, ma che potrebbe dare fastidio sul finale.</p>

<p>Se invece il bianco si sbilancia in <b>B8</b>... il nero può partire subito con una Stoner trap. Ed è effettivamente
    la sua risposta migliore. Il diagramma <span data-board-ref="berg-juigner-2025-37-ipotesi"></span> mostra
    una possibile continuazione.</p>

