<p>Le pedine <b>D5</b> ed <b>E4</b> sono nere e formano una piccola diagonale controllata dal nero.
Nel diagramma <span data-board-ref="esempio-1-evidenzia"></span> ho evidenziato la diagonale con il tratto nero.</p>

<board data-type="show" data-label="esempio-1-evidenzia" data-file="mini-linee-esempio-1-evidenzia.json"></board>

<p>Queste <i>mini-diagonali</i> sono molto importanti in fase di apertura perché limitano molto la mobilità
dell'avversario. Inoltre una mini-diagonale di questo tipo rappresenta sempre un posizione centrale e quindi
centripeta.</p>

<p>Nota che il bianco, in una posizione come questa, vorrebbe poter voltare una delle due pedine che formano la
mini-diagonale in modo da poter giocare una mossa ideale nella direzione opposta. Ma spesso non è facile ottenere
la giusta pedina di sponda e, provandoci, si ottiene solo di rafforzare la posizione di centro di chi la controlla.</p>

<p>Nella posizione proposta, per esempio, il bianco ha due possibilità di <i>tagliare</i> questa mini-diagonale.
Ne vedi il risultato nei diagrammi <span data-board-ref="esempio-1-B4"></span> e
<span data-board-ref="esempio-1-D2"></span>.</p>

