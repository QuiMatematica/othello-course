       data-caption="Berg - Juigner, Praga<br>Ipotesi con <b>38: B8</b>."></board>

<p>Per evitare di subire questo attacco, il bianco ha preferito giocare in <b>E8</b>.
    La Stoner trap non si è manifestata. Ma nei ragionamenti dei due giocatori c'è stata sicuramente.</p>

