<h1>Le Stoner trap</h1>

<p>Nelle pagine precedenti ti ho mostrato alcuni <b>attacchi alle formazioni di bordo</b>, in particolare:</p>
<ul>
    <li><a href="../../intermedio/attacchi-ai-5/chapter.php">attacchi ai cinque;</a></li>
    <li><a href="../../intermedio/altri-attacchi-ai-bordi/attacchi-alle-caselle-c.php">attacchi alle caselle C isolate;</a></li>
    <li><a href="../../intermedio/altri-attacchi-ai-bordi/attacchi-ai-4-piu-1.php">attacchi ai quattro più uno.</a></li>
</ul>

<p>Ma l'Othello permette anche altre forme di attacco ai bordi.</p>

<p>In questo capitolo vedremo un insieme di attacchi, con caratteristiche simili, che vanno sotto il nome di
<b>Stoner trap</b>.</p>

<p>Un esempio vale più di mille parole: vedi il diagramma <span data-board-ref="chapter-1"></span>.</p>

