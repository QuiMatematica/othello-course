<p>In effetti il nero non aveva molto altro da fare. Giocare in A7, per esempio,
significava lasciare al bianco muovere in H7, a cui sarebbe seguito H8 e G8. Il nero,
quindi, era costretto a muovere a nord-ovest, mentre il bianco conservava la sua regione
a nord-est.</p>

<h2>Caspard - Hoehne (WOC 2003)</h2>

<p>Il secondo esempio è tratto dalla partita tra Emmanuel Caspard e Andreas Hoehne
giocata ai Campionati Mondiali di Stoccolma, nel 2003, durante il match di spareggio
per l'accesso alle semifinali. Il commento è di Donato Barnaba ("Nero su Bianco", numero 2, giugno 2004).</p>

<p>Fino alla mossa 46 (diagramma 3), la partita ha seguito le linee ben note della
Tanida e si è sviluppata in condizioni di sostanziale equilibrio.</p>

