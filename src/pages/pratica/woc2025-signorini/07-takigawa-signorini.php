<h2>La partita commentata</h2>

<board data-type="show" data-label="commento" data-file="07-takigawa-signorini.json"
       data-caption="Partita commentata."></board>

<p>Probabilmente durante l'analisi ti sei reso conto che ho giocato questa partita senza un vero piano.
In particolare l'impressione è che ho giocato ogni mossa considerandola solo a profondità 1 o 2, ma senza
preoccuparmi di dove andare dopo.</p>

<p>In effetti è stato proprio così. Mi giustifico considerando che questa era la settima partita della
prima giornata di un Mondiale. Ma chiaramente se voglio arrivare a competizioni così elevate non posso
concedermi questi lussi.</p>

<p>Ebbene: che questo rimanga di lezione per me... e per tutti voi che avete letto questo commento.
Anche se siete stanchi, cercate comunque di elaborare un piano durante la partita. E soprattutto
ogni mossa deve essere analizzata in profondità. Almeno profondità 3, quindi pensando alla mossa,
alla miglior risposta dell'avversario e a una possibile propria contro-risposta.</p>

<h2>Tocca a te</h2>

<p>Analizzando la partita al computer risulta che non è mai stata particolarmente sbilanciata. Ho giocato male io ma ha
giocato male anche il mio avversario, e ci siamo rimbalzati il vantaggio più volte.</p>

<p>Alla mossa 37 (diagramma <span data-board-ref="quiz-2"></span>) secondo il computer ero in vantaggio. Però l'ho perso
    prendendo l'angolo <b>H8</b>. E, se ricordi
l'analisi, ho preso l'angolo per non perdere un tempo di gioco lasciando che l'avversario prendesse
il sei giocando in <b>B8</b>.</p>

<p>Però in effetti c'è un altro modo per non perdere quella mossa. Riesci a trovarlo?</p>

<board data-type="show" data-label="quiz-2" data-file="07-takigawa-signorini-quiz-2.json"></board>

<p>Se anche il nero non avesse preso il bordo sud, giocando a nord hai comunque ottenuto un vantaggio.
    Ricorda dall'analisi. Dopo <b>B3</b> non era più vantaggioso giocare in <b>F1</b> perché il nero
avrebbe potuto incunearsi in <b>E1</b>. Due buoni motivi (i tempi e il vantaggio a giocare) per giocare a nord
invece di prendere il bordo.</p>

<p>Anche alla mossa 45 ero io in leggero vantaggio, ma il finale perfetto non era per niente facile.</p>

