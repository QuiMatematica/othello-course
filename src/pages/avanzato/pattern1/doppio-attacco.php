<p>Ho visto molti giocatori, in questa situazione, prendere direttamente l'angolo, per il timore di perderne
l'accesso. Il problema di muovere in <b>A8</b> è che il bianco potrà incunearsi in <b>E8</b> e il nero
resterà a corto di mosse (diagramma <span data-board-ref="doppio-attacco-2"></span>).</p>

<board data-type="show" data-label="doppio-attacco-2" data-file="doppio-attacco-2.json"></board>

<p>&Egrave; molto meglio per il nero giocare egli stesso in <b>E8</b>, al fine di creare un doppio attacco
all'angolo (diagramma <span data-board-ref="doppio-attacco-3"></span>).</p>

<board data-type="show" data-label="doppio-attacco-3" data-file="doppio-attacco-3.json"></board>

<p>Poiché un doppio attacco all'angolo permette quasi sempre di conquistare l'angolo, se tale angolo è utile,
non devi esitare a sacrificare un bordo. Nel diagramma <span data-board-ref="doppio-attacco-4"></span> potresti
essere tentato di giocare <b>G7</b>, <b>H7</b> o qualche altra mossa del genere, ma muovendo in <b>E8</b>
puoi ottenere una facile vittoria.</p>

