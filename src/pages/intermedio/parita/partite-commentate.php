<h1>Parità: partite commentate</h1>

<h2>Seeley - Suekuni (WOC 2004)</h2>

<p>Il primo esempio è tratto dalla seconda partita della finale del Campionato del
Mondo del 2004, tenutosi a Londra. La partita è tra l'americano Ben Seeley (nero)
e il giapponese Makoto Suekuni (bianco). Il bianco ha scelto fin dalle prime mosse
di uscire da un'apertura pari, sperando di cogliere l'avversario impreparato.</p>

<p>Dopo la mossa 48 giungono alla posizione mostrata nel diagramma 1.</p>

