<h2>Il <i>pattern</i></h2>

<p>Chiaramente una manovra così complessa ha bisogno di diversi elementi per essere messa in atto così come è
stata presentata.</p>

<p>Il <i>pattern</i> (diagramma <span data-board-ref="vittoria-senza-angoli-2"></span>) che permette questo <i>tesuji</i>
    prevede la presenza di:</p>
<ul>
    <li>quattro regioni di quattro caselle in cui il bianco non ha accesso (riquadri rossi);</li>
    <li>due bordi pieni, ovvero con il controllo del controbordo da parte del colore attaccato (riquadri blu);</li>
    <li>la possibilità di muovere nella casella <b>X</b> (freccia gialla);</li>
    <li>la possibilità di muovere nelle due caselle <b>C</b> della stessa regione (frecce viola).</li>
</ul>

