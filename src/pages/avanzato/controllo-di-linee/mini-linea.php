<h1>Controllo di mini-linee</h1>

<p>Nelle pagine precedenti abbiamo visto l'importanza di controllare le linee più critiche soprattutto nella fase
finale della partita. Tali linee critiche sono le diagonali principali, i controbordi e le diagonali C.</p>

<p>Tuttavia il controllo di linee può essere utilizzato fin dall'apertura.</p>

<p>Osserva, per esempio, la posizione del diagramma <span data-board-ref="esempio-1"></span>.</p>

