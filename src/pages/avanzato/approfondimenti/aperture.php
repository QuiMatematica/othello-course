<p>Sì, la logica che sta dietro alle aperture nell'Othello è molto simile a quella di una tossicodipendenza. Conoscere
    bene le aperture può portare un vantaggio enorme, non fraintendetemi. Soprattutto oggi, con tutti i software
    disponibili che rendono facile e veloce studiare l'efficacia di una mossa all'inizio della partita, invece di
    perdere ore valutando ogni variante sul tavolo. Inizi a giocare a Othello e la prima cosa che vuoi fare è giocare
    il più vicino possibile a un computer. Il che significa fare sempre le "mosse giuste", specialmente all'inizio. Ti
    appassioni, magari ti rendi conto di avere una buona memoria e una discreta capacità di riconoscimento degli schemi
    e, in pochi mesi, potresti finire per conoscere le aperture meglio di giocatori con decenni di esperienza. La cosa
    ti entusiasma, perché capisci che se riesci a trascinare quei giocatori in una situazione equilibrata a 20 mosse
    dalla fine, loro hanno solo una manciata di possibilità di vincere la partita. Per loro è un vero azzardo, e spetta
    a loro uscirne, magari rischiando un -6 da qualche parte a metà partita. Voglio dire, senza la mia ossessione per
    le aperture, non avrei pareggiato contro David Hand l'anno scorso.</p>

