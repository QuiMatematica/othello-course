tutte le mosse ha ancora speranze di vincere la partita.</p>

<h2>Esaurire l'area di gioco</h2>

<p>Facciamo un'analisi più approfondita di questa situazione, osservando la schematizzazione della posizione
    del diagramma 1 riportata nel digramma 6.</p>

