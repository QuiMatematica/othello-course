<p>Non è esterofilia, ma ci sono alcuni termini che ormai sono entrati nel lessico comune del gioco, e se venissero tradotti
    perderebbero il loro potere evocativo.</p>

<div class="card border-primary mb-3">
    <div class="card-header">Definizione</div>
    <div class="card-body">
        <p class="card-text">Con <b><i>pattern</i></b> indichiamo uno schema, una configurazione ricorrente.</p>
    </div>
</div>

<p>Sono <i>pattern</i>, per esempio, i cinque sui bordi e le configurazione di bordo in generale.
    Sono <i>pattern</i> anche le posizioni in cui è presente il controllo di una linea o di una diagonale.</p>

<p>&Egrave; importante conoscere i <i>pattern</i> perché la stessa configurazione, o una simile con le stesse
    caratteristiche,
    va sempre affrontata nello stesso modo. Sapere cosa fare quando si presenta un determinato <i>pattern</i> permette
    di
    individuare più facilmente e velocemente la mossa giusta da applicare.</p>

<div class="card border-primary mb-3">
    <div class="card-header">Definizione</div>
    <div class="card-body">
        <p class="card-text"><b><i>Tesuji</i> (手筋)</b> (si pronuncia <i>tesugi</i>) è una parola giapponese usata in giochi
            come il Go e lo Shogi. La possiamo tradurre come <i>sequenza di mosse brillanti</i>. I <i>tesuji</i> sono le
            mosse giuste
            da giocare quando si presenta un certo <i>pattern</i>.</p>
    </div>
</div>

<p>Un <i>tesuji</i>, per esempio, è una Stoner trap applicata a un quattro sbilanciato. Il quattro sbilanciato è il <i>pattern</i>.
    La Stoner trap è il <i>tesuji</i>: la sequenza di mosse brillanti che va a sfruttare le proprietà del <i>pattern</i>.
</p>

<p>Uno stesso <i>pattern</i> può portare a più di un <i>tesuji</i>. Per esempio abbiamo visto come un cinque può essere
    attaccato sia da una casella X sia da una casella C.</p>

