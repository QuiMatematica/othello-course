<p>&Egrave; necessario,
a questo punto, analizzare punti di forza e punti deboli dei due colori:</p>

<ul>
  <li>il nero ha la parità in due regioni dispari (nord-ovest e sud-ovest);</li>
  <li>il bianco ha la parità in una regione dispari (nord-est);</li>
  <li>la regione di sud-est è dispari, ma entrambe i giocatori possono
  accedervi;</li>
  <li>il nero ha un cinque sul bordo est che è già stato attaccato.</li>
</ul>

<p>Tocca al nero. Guardando solamente alla parità dovrebbe cercare di conservare inalterate,
il più a lungo possibile, le due aree a ovest e giocare nell'area dispari
a sud-est. Ma giocare qui significa muovere in H7 o in G8: tra le due la "meno peggio"
è sicuramente la prima, che fa perdere solo il bordo est.</p>

<p>Ma cos'è più importante? Il bordo o la parità? Ebbene, come l'americano sa molto
bene, è molto più importante la parità che, a fine partita, può dare ben di più delle
otto pedine perse sul bordo. Il diagramma 2 mostra il finale giocato (che è perfetto).</p>

