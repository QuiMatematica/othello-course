<p class="small-caps"><b>Spoiler: la risposta non è un numero</b></p>

<p>Una volta, quando ero giovane e ingenuo e studiavo ancora dai libri per diventare bravo a scacchi, lessi di quella
    famosa citazione dello scacchista cecoslovacco Richard Retí, che quando gli si chiedeva quante mosse calcolava
    normalmente in partita, rispondeva "di norma, nemmeno una." Lui ci teneva a dire che era sincero quando rispondeva
    così. Quella risposta aveva un senso ben preciso: serviva a sottolineare che la pura predizione non è l'unico metodo
    per giocare bene, né necessariamente il più importante, ma che esistono altri approcci che da umani usiamo per
    eccellere in giochi complessi come scacchi, Othello, o Go.</p>

