<p>Queste <i>mini-diagonali</i> garantiscono almeno tre vantaggi:</p>

<ul>
    <li>limitano la mobilità dell'avversario: nell'esempio il bianco non può giocare né in <b>C6</b> né in <b>F3</b>;
    è come se a est e a ovest il bianco avesse due muri da quattro pedine;</li>
    <li>rappresentano sempre un posizione centrale e quindi centripeta: nell'esempio il nero ha il controllo del centro
        e il bianco ha un'alta influenza;</li>
    <li>rendono difficile all'avversario prendere il controllo del centro: se, in una posizione simile a quella
        dell'esempio, il bianco potesse a giocare in <b>C6</b> o in <b>F3</b>, non volterebbe le pedine lungo la mini-diagonale
    controllata dal nero.</li>
</ul>

<p>In una posizione come questa il bianco vorrebbe poter voltare una delle due pedine che formano la
mini-diagonale in modo da poter giocare una mossa ideale nella direzione opposta. Ma spesso non è facile ottenere
la giusta pedina di sponda e, provandoci, si ottiene solo di rafforzare la posizione di centro di chi la controlla.</p>

<p>Nella posizione proposta, per esempio, il bianco ha due possibilità di <i>tagliare</i> questa mini-diagonale.
Ne vedi il risultato nei diagrammi <span data-board-ref="esempio-1-B4"></span> e
<span data-board-ref="esempio-1-D2"></span>.</p>

