<h1>Le Stoner trap sono rare?</h1>

<p>Mi ha colpito un'affermazione che il maestro Paolo Bianchi ha scritto sulla chat nazionale della F.N.G.O.:</p>

<p class="text-center"><i>Belle le Stoner, ma anche molto rare a verificarsi.
        Nelle centinaia di partite giocate ne ho subito pochissime, forse solo una, da Caspard
        in un torneo milanese degli anni '90.</i></p>

<p>Ho cercato di ricordarmi quante Stoner trap ho giocato o subìto nelle mie partite.</p>

<p>Me ne ricordo molto bene una. 22 aprile 1990: avevo 14 anni. Ero al mio secondo torneo
ospite a Modena dei simpaticissimi Angela Quattrocchi e Luca Ottani. Stavo giocando proprio contro Angela, che in quel momento era
    in testa alla classifica. E all'improvviso
eccola lì: la possibilità di giocare la mia prima Stoner trap. E ho vinto la partita.
    Purtroppo non ho più il referto, quindi non posso mostrartela.</p>

<p>Altre... no! Certo non ricordo a memoria tutte le mie partite, ma ho un ricordo più o meno vago di alcune tra le
    più significative.
E non ricordo nessun'altra Stoner trap.</p>

<p>Ma allora è vero che sono così rare? Al punto che, forse, non vale la pena studiarle?</p>

<h2>Stoner trap: the phantom menace</h2>

<p>Sì, la citazione non è casuale. <i>The phantom menace</i>: <i>la minaccia fantasma</i>, il titolo del primo episodio
della saga di Guerre Stellari.</p>

<p>Anche se non si vedono, le Stoner trap sono spesso dietro l'angolo. Sono una minaccia che può concretizzarsi
    da un momento all'altro. Ma <i>se le conosci le eviti</i>. E se le eviti, non si presentano in partita.</p>

<p>Ma stai pur certo che se in una partita si prefigura una formazione sbilanciata, entrambi pensano
alle Stoner trap: l'uno per capire se può utilizzarla, l'altro per fare di tutto per evitarla.</p>

<p>Come fare per evitare di subire una Stoner trap?</p>

<h2>Evitare di sbilanciarsi</h2>

<p>Il diagramma <span data-board-ref="berg-juigner-2025-37"></span>
    mostra la posizione dopo la mossa 37 della prima finale del Campionato Europeo 2025
    tra Matthias Berg (nero) e Arthur Juigner (bianco).</p>

<board data-type="show" data-label="berg-juigner-2025-37" data-file="berg-juigner-2025-37.json"
       data-caption="Berg - Juigner<br>Campionato Europeo 2025"></board>

<p>Il bianco deve necessariamente rispondere sul bordo sud, pena la perdita di due tempi di gioco. Ha due mosse tra cui
scegliere: incunearsi in <b>E8</b> o sbilanciarsi in <b>B8</b>.</p>

<p>Giocando in <b>E8</b> si forma una posizione che è nota come <i>Suekuni trap</i>, che studieremo più avanti nel corso.
E' una posizione svantaggiosa per il bianco, in particolare se si dovesse trovare ad avere poche mosse: rischia di essere costretto
    a giocare in <b>B8</b> o in <b>G8</b>, lasciando l'intero bordo all'avversario. In questo momento la mobilità del bianco
è buona, anche se ha un'influenza piuttosto alta. Il rischio però rimane nel finale, quando ci saranno poche caselle libere
    e quindi poche mosse a disposizione. Per riassumere, diciamo che è una posizione
    scomoda, non particolarmente pericolosa in questo momento, ma che potrebbe dare fastidio nel finale.</p>

<p>Se invece il bianco si sbilancia in <b>B8</b>... il nero può partire subito con una Stoner trap.
    Il diagramma <span data-board-ref="berg-juigner-2025-37-ipotesi"></span> mostra
    una possibile continuazione.</p>

