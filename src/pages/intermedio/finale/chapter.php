<h1>Il finale</h1>

<p>Siamo alle battute conclusive della partita: mancano una quindicina di mosse e la tavola è
    ormai quasi piena. Questo è il momento decisivo, in cui bisogna trasformare in vittoria i
    vantaggi conquistati nella fase di apertura e nel centro partita. Ma attenzione: ogni errore
    adesso è irreversibile e potrebbe compromettere tutto il lavoro fatto finora, portandoti
    a una sconfitta amara. Vediamo quindi come affrontare al meglio questa fase cruciale del
    gioco.</p>

<h2>Il finale perfetto</h2>

<p>In teoria, data una posizione può essere calcolato il
    <a href="ultime-due-mosse.php">finale perfetto</a>.
    Nelle ultime cinque o sei mosse, il processo è relativamente semplice; con più caselle libere,
    invece, la complessità aumenta rapidamente. I computer, con la loro velocità e capacità di
    memoria, ci superano in questo compito, ma è comunque utile imparare a riconoscere e
    contare il finale perfetto, almeno nelle mosse decisive.</p>

<h2>Il finale umano</h2>

<p>Ma come fare quando abbiamo una decina di caselle libere, il vantaggio è minimo, e -
    ovviamente - vogliamo vincere la partita?
    Abbiamo bisogno di strumenti che ci permettano di selezionare le sequenze più
    vantaggiose: ci servono delle euristiche.</p>

<div class="card border-primary mb-3">
	<div class="card-header">Definizione</div>
	<div class="card-body">
        <p class="card-text">Le <b>euristiche</b> (dal greco <i>heurískein</i>: trovare, scoprire) sono procedimenti mentali
            intuitivi e sbrigativi, scorciatoie mentali, che permettono di costruire un’idea generica su un argomento
            senza effettuare troppi sforzi cognitivi. Sono strategie veloci utilizzate di frequente per giungere
            rapidamente a delle conclusioni.</p>
	</div>
</div>

<p>In questo capitolo scoprirai le euristiche più efficaci per il finale di partita:</p>
<ul>
    <li><a href="parita.php">la parità</a>: la più importante in finale, già incontrata
        in precedenza e che approfondiremo anche in questo capitolo;</li>
    <li><a href="contare.php">selezione delle sequenze</a>: invece di contare ogni possibilità,
        impareremo a concentrarci solo sulle mosse più significative;</li>
    <li><a href="sequenze-indipendenti.php">dipendenza tra aree</a>: capiremo quando le mosse in
        zone diverse della tavola sono collegate e come sfruttare queste connessioni a nostro
        vantaggio.</li>
</ul>

<p>Altre euristiche verranno presentate nella sezione avanzata del corso.</p>

<p>Per migliorare davvero nel finale, l’allenamento è fondamentale. Esistono software che
    propongono posizioni da risolvere con la strategia perfetta (o almeno vincente) entro un
    tempo limitato: esercitarsi con questi strumenti può fare la differenza tra un buon
    giocatore e un maestro.</p>

<p>Chiudiamo il capitolo con qualche riflessione matematica sulla
    <a href="complessita-othello.php">complessità</a> del gioco.</p>
