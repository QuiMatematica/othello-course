<h1>Il finale</h1>

<p>Mancano una quindicina di mosse alla fine e oramai la tavola da gioco offre pochi
spazi liberi. E' il momento in cui bisogna concretizzare i vantaggi di posizione
faticosamente ottenuti durante l'apertura e il centropartita, ma è anche quello
in cui ogni errore avrà la caratteristica dell'inderogabilità. Spesso un finale giocato
male vanifica tutto ciò che di buono si è fatto fino a quel momento e conduce a una
misera sconfitta. Vediamo dunque come è meglio comportarsi in questo cruciale
momento della partita.</p>

<p>Innanzitutto è buona cosa capire che è teoricamente possibile determinare
il <a href="ultime-due-mosse.php">finale perfetto</a>. Il procedimento non è semplice e rimane
alla portata solo dei calcolatori, ma è comunque da conoscere e applicare almeno nelle
ultime mosse.</p>

<p>Verranno poi presentati alcuni accorgimenti per sfruttare al meglio la posizione
raggiunta. Vedremo ancora una volta come angoli, caselle C e caselle X hanno un 
grande valore strategico.</p> 

<p>Oltre a queste è necessario avere molto intuito ed esperienza! Per questo, per
ottenere un ottimo livello di gioco, può essere buona cosa allenarsi
costantemente sui finali, utilizzando programmi che propongono posizioni da risolvere in modo
    perfetto (o per lo meno vincente) in un tempo limitato.</p>
