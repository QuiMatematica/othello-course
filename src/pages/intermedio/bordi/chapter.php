        <h1>I bordi</h1>

        <p>Fin dalla prime partite ci si rende conto che i bordi della scacchiera rivestono un
        ruolo particolare. Se gli angoli sono strategicamente fondamentali perché rappresentano
        posizioni stabili, anche i bordi sono caratterizzati da una certa stabilità.</p>

        <p>Per esempio una pedina posta sul bordo rappresentato dalla prima riga può essere
        girata solo se l'avversario pone due pedine sulla stessa riga. Le pedine sui bordi,
        dunque, sono più facili da controllare delle pedine centrali e il giocatore che per
        primo pone una pedina su un bordo è facile che riesca ad allungare il suo possesso
        a tutto il bordo.</p>

        <p>Queste caratteristiche sono state evidenziate anche parlando delle pedine stabili:
        le caselle C (che fanno parte dei bordi) sono meno pericolose delle caselle X proprio
        per la loro parziale stabilità e facilità di controllo.</p>

        <p>I rischi non mancano... Tutte le formazioni di bordo sono attaccabili: è possibile
        cioè sacrificare una posizione strategica (quasi sempre un angolo) per ottenere maggiori
        vantaggi (un angolo e il bordo o uno più tempi di gioco tanto per citare alcuni esempi).

        <p>&Egrave; fondamentale quindi, fin da subito, convincersi che <b>i bordi non sono fondamentali
        per la vittoria</b> e che, anzi, <b>spesso rappresentano un punto debole</b>. Questo è un
        errore tipico dei neofiti che sottovalutano l'importanza dei tempi di gioco e degli
        attacchi. Non è vero neanche il contrario, comunque: spesso è importante controllare i bordi
        perché possono diventare inattaccabili o possono permettere di guadagnare dei preziosi
        tempi di gioco. In breve: ogni situazione va ben esaminata, valutando vantaggi e svantaggi.</p>

        <h2>Obiettivi del gioco sui bordi</h2>

        <p>Il gioco sui bordi deve essere guidato da tre obiettivi principali:</p>

        <ul>
          <li>conquistare dei <b>tempi di gioco</b> e non consentire all'avversario di farlo;</li>
          <li>conquistare delle <b>posizioni non attaccabili</b> o costringere l'avversario a prendere
          <b>posizioni attaccabili</b>;</li>
          <li>preparare degli spazio adeguati per giocare la <b>parità</b>.</li>
        </ul>

        <p>Del primo obietto si è già parlato nella
        <a href="../tempi-di-gioco/chapter.php" title="I tempi di gioco">lezione precedente</a>.
        Il terzo obiettivo riguarda la parità che verrà spiegata nel
        prossimo capitolo.</p>

        <p>In questa lezione verrà trattato in modo approfondito il secondo obiettivo:
        dopo una prima presentazione delle diverse formazioni di bordo verranno mostrate
        tutte le forme più importanti di attacchi e trappole.</p>